function disc_db(): PDO {
    static $pdo = null;
    if ($pdo !== null) return $pdo;
    $dir = __DIR__ . '/data';
    if (!is_dir($dir)) {
        mkdir($dir, 0750, true);
        file_put_contents($dir . '/.htaccess', "Deny from all\nOptions -Indexes\n");
    }
    $pdo = new PDO('sqlite:' . $dir . '/disc.sqlite');
    $pdo->setAttribute(PDO::ATTR_ERRMODE, PDO::ERRMODE_EXCEPTION);
    $pdo->exec("
        CREATE TABLE IF NOT EXISTS disc_results (
            id             INTEGER PRIMARY KEY AUTOINCREMENT,
            agent_id       INTEGER,
            name           TEXT NOT NULL,
            email          TEXT NOT NULL,
            phone          TEXT NOT NULL DEFAULT '',
            role           TEXT NOT NULL DEFAULT '',
            answers        TEXT NOT NULL,
            score_d        INTEGER NOT NULL,
            score_i        INTEGER NOT NULL,
            score_s        INTEGER NOT NULL,
            score_c        INTEGER NOT NULL,
            primary_style  TEXT NOT NULL,
            secondary_style TEXT,
            token          TEXT NOT NULL UNIQUE,
            completed_at   TEXT NOT NULL DEFAULT (datetime('now'))
        );
        CREATE INDEX IF NOT EXISTS idx_disc_token ON disc_results(token);
        CREATE INDEX IF NOT EXISTS idx_disc_email ON disc_results(email);
    ");
    return $pdo;
}

function disc_save(array $d): string {
    $db    = disc_db();
    // Migrate existing installs that predate phone/role columns
    try { $db->exec("ALTER TABLE disc_results ADD COLUMN phone TEXT NOT NULL DEFAULT ''"); } catch (\Exception $e) {}
    try { $db->exec("ALTER TABLE disc_results ADD COLUMN role  TEXT NOT NULL DEFAULT ''"); } catch (\Exception $e) {}

    $token = bin2hex(random_bytes(16));
    $st = $db->prepare("
        INSERT INTO disc_results
            (agent_id,name,email,phone,role,answers,score_d,score_i,score_s,score_c,primary_style,secondary_style,token)
        VALUES
            (:aid,:name,:email,:phone,:role,:ans,:sd,:si,:ss,:sc,:ps,:sec,:tok)
    ");
    $st->execute([
        ':aid'   => $d['agent_id'] ?? null,
        ':name'  => $d['name'],
        ':email' => $d['email'],
        ':phone' => $d['phone'] ?? '',
        ':role'  => $d['role']  ?? '',
        ':ans'   => json_encode($d['answers']),
        ':sd'    => $d['score_d'],
        ':si'    => $d['score_i'],
        ':ss'    => $d['score_s'],
        ':sc'    => $d['score_c'],
        ':ps'    => $d['primary_style'],
        ':sec'   => $d['secondary_style'] ?? null,
        ':tok'   => $token,
    ]);
    return $token;
}

