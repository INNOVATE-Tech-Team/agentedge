function disc_by_token(string $token): ?array {
    $st = disc_db()->prepare("SELECT * FROM disc_results WHERE token=? LIMIT 1");
    $st->execute([$token]);
    return $st->fetch(PDO::FETCH_ASSOC) ?: null;
}

function disc_all(int $limit = 500): array {
    $st = disc_db()->query("SELECT * FROM disc_results ORDER BY completed_at DESC LIMIT {$limit}");
    return $st->fetchAll(PDO::FETCH_ASSOC);
}
